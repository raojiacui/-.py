# 选择控制
#
# if 可以根据情况选择性执行某些语句
# 例如下方代码
#
# 定义一个变量 grade 代表年级
grade = 3
# 如果 grade 小于 7
if grade < 7:
    # 这句 print 只在 grade 小于 7 这个条件满足的情况下会被执行
    print('小学生')


# 选择控制有多种不同的用法
# 只有 if
if 1 > 2:
    print('条件成立')


# if 带 else
# if else 必定会执行一个语句
if 1 > 2:
    print('条件成立')
else:
    print('条件不成立')


# 多个 if else
grade = 8
if grade < 7:
    print('小学生')
elif grade < 10:
    print('初中生')
else:
    print('高中生')


