# 函数返回值
#
# 函数不仅可以合并操作重复性的代码
# 还可以通过计算得到一个结果, 结果就是返回值
# 函数可以有「返回值」
# 返回值的意思是函数调用的结果是一个值, 可以被赋值给变量

# 例
# 定义一个函数 add
# 接受 a b 两个参数
def add(a, b):
    # 用 return 语句来得到一个返回值
    # 函数执行到 return 语句的时候就结束了
    return a + b


# 用法
print('add 函数的返回值', add(1, 2))
number = add(2, 3)
print('add 函数的返回值 number', number)

# 注意看上面的语句, add(2, 3) 被当做一个值赋值给了变量 number
# 也就是说这个函数调用是一个数值, 数值的值就是函数的返回值
# 例如, 使用函数来求绝对值
def abs(n):
    if n < 0:
        n = -n
    return n


print(abs(0))
print(abs(-8))
print(abs(3))

