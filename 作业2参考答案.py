# ==================== 作业 9: 冈比亚国旗 ====================

# 作业 9
# 画 冈比亚国旗
# gambia()

# 提示:
#     冈比亚国旗和德国国旗类似, 中间的矩形由一个大纵向矩形和两个小纵向矩形组成,
#     所以画 5 个矩形

# 分步提示:
#     1. 最上面和最下面两个矩形和德国国旗一致
#     2. 中间的矩形分成三个矩形, 宽度占比分别为 1:6:1, 分别计算出这三个矩形的中心坐标
#     3. 分别计算中间三个矩形的长度和高度
#     4. 使用 center_rect 画出 5 个矩形, 每次传入的参数不一致

def gambia():
    # 国旗矩形长宽 200 * 150, 左上角坐标 (-100, -101)
    # 矩形范围: x[-100, 100], y[-101, 49]
    x = -100
    y = -101
    w = 200
    h = 150
    # 从上到下，高度比例为 4:1:15:1:4
    total = 4 + 1 + 15 + 1 + 4  # = 25
    h1 = h * 4 / total  # 上横条 = 24, y范围[-101, -77]
    h2 = h * 1 / total  # 白边 = 6, y范围[-77, -71]
    h3 = h * 15 / total  # 中间 = 90, y范围[-71, 19]
    h4 = h * 1 / total  # 白边 = 6, y范围[19, 25]
    h5 = h * 4 / total  # 下横条 = 24, y范围[25, 49]
    # 计算中心 x 坐标
    cx = x + w / 2  # = 0
    # 上横条 (红色), 中心y = -101 + 12 = -89
    center_rect(cx, y + h1 / 2, w, h1)
    # 中间3个纵向矩形，宽度占比为 1:6:1（横向分布）
    # 中间区域 y 范围 [-71, 19], 高度 90, 中心 y = -26
    middle_total = 1 + 6 + 1  # = 8
    w1 = w * 1 / middle_total  # = 25, x范围[-100, -75]
    w2 = w * 6 / middle_total  # = 150, x范围[-75, 75]
    w3 = w * 1 / middle_total  # = 25, x范围[75, 100]
    middle_y = y + h1 + h2 + h3 / 2  # = -101 + 24 + 6 + 45 = -26
    # 左矩形 (绿色), 中心(-87.5, -26)
    center_rect(x + w1 / 2, middle_y, w1, h3)
    # 中矩形 (蓝色), 中心(0, -26)
    center_rect(x + w1 + w2 / 2, middle_y, w2, h3)
    # 右矩形 (绿色), 中心(87.5, -26)
    center_rect(x + w1 + w2 + w3 / 2, middle_y, w3, h3)
    # 下横条 (红色), 中心y = -101 + 24 + 6 + 90 + 6 + 12 = 37
    center_rect(cx, y + h1 + h2 + h3 + h4 + h5 / 2, w, h5)


