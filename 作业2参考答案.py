# ==================== 作业 11: 朝鲜国旗 ====================

# 作业 11
# 画朝鲜国旗
# northkorea()

# 提示:
#     朝鲜国旗从上往下依次为蓝色矩形、白色矩形、红色矩形、白色矩形和蓝色矩形,
#     这些矩形的高度比分别为 4:1:15:1:4, 红色矩形里包含了一个圆形和一个五角星
#     圆形的直径和红色矩形的高度的比为 2:3, 使用这些数据计算出各个图形的坐标, 然后画出来

# 分步提示:
#     1. 分别计算出 5 个矩形的坐标和长宽
#     2. 计算出圆形的圆心, 假设圆心距离国旗矩形左边的距离与国旗的长度(200) 的比值为 3:8
#     3. 调用 5 次 rect 函数画 5 个矩形
#     4. 调用 circle 函数画圆形
#     5. 调用 wujcxy 函数画五角星

def northkorea():
    # 国旗矩形长宽 200 * 150, 左上角坐标 (-100, -101)
    x = -100
    y = -101
    w = 200
    h = 150
    # 从上到下，高度比例为 4:1:15:1:4
    total = 4 + 1 + 15 + 1 + 4
    h1 = h * 4 / total  # 蓝色
    h2 = h * 1 / total  # 白色
    h3 = h * 15 / total  # 红色
    h4 = h * 1 / total  # 白色
    h5 = h * 4 / total  # 蓝色
    # 计算各矩形左上角 y 坐标
    y1 = y
    y2 = y + h1
    y3 = y + h1 + h2
    y4 = y + h1 + h2 + h3
    y5 = y + h1 + h2 + h3 + h4
    # 画 5 个矩形
    rect(x, y1, w, h1)
    rect(x, y2, w, h2)
    rect(x, y3, w, h3)
    rect(x, y4, w, h4)
    rect(x, y5, w, h5)
    # 圆形的直径和红色矩形的高度的比为 2:3
    circle_radius = h3 * 2 / 3 / 2
    # 圆心距离国旗矩形左边的距离与国旗的长度(200) 的比值为 3:8
    circle_x = x + w * 3 / 8
    circle_y = y + h1 + h2 + h3 / 2
    # 画圆形
    circle(circle_x, circle_y, circle_radius)
    # 画五角星
    wujcxy(circle_x, circle_y, circle_radius * 0.6)


