# 画矩形 (x, y 是左上角坐标)
def rect(x, y, w, h):
    jump(x, y)
    i = 0
    while i < 2:
        forward(w)
        right(90)
        forward(h)
        right(90)
        i = i + 1


# ==================== 作业 1: 圆形函数 ====================

# 作业 1
# 实现一个圆形函数
# x y 是圆形的圆心
# r 是半径
# circle(x, y, r)

# 提示:
#     我们在第一节课的最后有一个作业 polygon, 实际上电脑是通过模拟来实现画一个圆的
#     也就是画一个正 n 变形, n 很大的时候, 这个图形就可以看做是一个圆
#     有一个问题需要注意, 初始点在圆心处, 在画 polygon 的时候需要从左上角开始
#     所以要先从圆心移动到左上角(请看分步提示)

# 分步提示:
# 注意, 你需要 import math 后才能使用 math.pi
#     1. 我们用正 36 边形来模拟圆形, 也就是说 n 取值 36
#     2. 正多边形的周长是 n(边数) * l(边长), 而它的外接圆的周长是 2 * math.pi * r, 我们这里把这两个周长看做相等(因为我们只是用正多边形近似模拟, 不是精准画圆, 小小偏差可以接受)
#        所以直接算出 l = (2 * math.pi * r) / n  这个公式中 l 是多边形边长, r 是圆半径, n 是边数
#        具体请看下图(正多边形和外接圆)
#     3. 初始情况下箭头是朝右的, 所以先往左转, 左转 90 度等于右转 270 度, 所以你需要自己实现一个 left 函数(或者直接 right(270))
#         这个角度的具体计算原理见下图
#         这里直接把公式给出如下, 你可以直接使用
#         jcdu = (90 + (360 / n) / 2)
#         left(jcdu)
#     4. 转完之后, 移动 r, 也就是半径的距离
#     5. 这样就移动到了左上角的位置, 但此时角度并不是朝右的,
#        刚刚往左转动了 jcdu, 现在往右转回来这个角度
#        right(jcdu)
#     6. 这时候你就在正多边形的顶部横边的左顶点并且方向朝右了, 以此为起点画一个正 36 边形即可

def circle(x, y, r):
    # 用正 36 边形来模拟圆形
    n = 36
    # 计算边长: 周长 = n * l, 外接圆周长 = 2 * pi * r
    l = (2 * math.pi * r) / n
    # 计算转向角度
    jcdu = (90 + (360 / n) / 2)
    # 从圆心移动到左上角
    jump(x, y)
    left(jcdu)
    forward(r)
    right(jcdu)
    # 画正 36 边形
    i = 0
    while i < n:
        forward(l)
        right(360 / n)
        i = i + 1


# ==================== 作业 2: 中心矩形函数 ====================

# 作业 2
# 实现一个矩形函数
# x y 是矩形中心的坐标
# w h 是宽高
# center_rect(x, y, w, h)

# 提示:
#     在第一课的作业中已经实现了 rect 函数,
#     不过 rect 函数中的 x, y 是表示左上角坐标,
#     现在需要实现的 center_rect 函数的 x, y 是矩形中心的坐标,
#     所以应该先从矩形中心移动到矩形左上角, 然后调用 rect 函数

# 分步提示:
#     1. 根据矩形中心坐标 x, y 计算出左上角坐标 x1, y1
#     2. 调用 rect 函数, 传入的参数分别为左上角坐标, 宽和高

def center_rect(x, y, w, h):
    # 根据中心坐标计算左上角坐标
    x1 = x - w / 2
    y1 = y - h / 2
    # 调用 rect 函数
    rect(x1, y1, w, h)


# ==================== 作业 3: 正五角星(顶部横边左边点) ====================

# 作业 3
# 实现一个正五角星(国旗大星星那种)函数
# x y 是五角星顶部横边的左边点坐标
# length 是一条线的长度
# 这是一个正五角星
# vgwujcxy(x, y, length)

# 分步提示:
#     1. 正五角星需要重复 5 次, 所以需要循环 5 次
#     2. 每次循环中前进 length 的长度, 右转 144 度
#     3. 别忘了循环的时候 i 增加 1

def vgwujcxy(x, y, length):
    jump(x, y)
    i = 0
    while i < 5:
        forward(length)
        right(144)
        i = i + 1


