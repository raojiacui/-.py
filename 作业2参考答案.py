    # 直径2格, 半径1格
    small_star_r = 1 * gw
    wujcxy(x + 10 * gw, y + 2 * gh, small_star_r)
    wujcxy(x + 12 * gw, y + 4 * gh, small_star_r)
    wujcxy(x + 12 * gw, y + 7 * gh, small_star_r)
