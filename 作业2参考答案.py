# ==================== 辅助函数 ====================

def sin(degree):
    # 如上课所述, 数学库里面的 sin 函数接受弧度作为参数
    # 我们这个函数接受角度, 下面是弧度转角度的公式
    radians = degree * math.pi / 180
    return math.sin(radians)


def cos(degree):
    radians = degree * math.pi / 180
    return math.cos(radians)


def left(degree):
    right(360 - degree)


